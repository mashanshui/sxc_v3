package com.sxcapp.www.UserCenter.Bean;

import android.content.Context;
import android.util.Log;
import android.widget.Toast;

import io.reactivex.Observer;
import io.reactivex.disposables.Disposable;

/**
 * Created by wenleitao on 2017/7/10.
 */

public abstract class UserCodeObserver<T> implements Observer<UserCodeResult<T>> {

    private static final String TAG = "CodeObserver";
    private Context mContext;

    protected UserCodeObserver(Context context) {
        this.mContext = context.getApplicationContext();
    }

    @Override
    public void onSubscribe(Disposable d) {

    }

    @Override
    public abstract void onNext(UserCodeResult<T> value);

    @Override
    public void onError(Throwable e) {
        Log.e(TAG, "error:" + e.toString());
    }

    @Override
    public void onComplete() {
        Log.d(TAG, "onComplete");
    }


    protected abstract void onHandleSuccess(T t);

    protected void onHandleError(String msg) {
        Toast.makeText(mContext, msg, Toast.LENGTH_SHORT).show();
    }
}